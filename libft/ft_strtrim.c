#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*dest;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen((char*)s1);
	while (len && ft_strrchr(set, s1[len]))
		len--;
	dest = ft_substr(s1, 0, (len + 1));
	return (dest);
}
