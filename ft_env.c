#include "shell.h"

void	ft_env(char **env)
{
	int	i;

	i = 0;
	while (env[i])
	{
		if (!ft_strchr(env[i], '='))
		{
			i++;
			continue ;
		}
		ft_putendl_fd(env[i], 1);
		i++;
	}
	exit(0);
}
